// Diferença entre var, let e constante
"use strict"

// VAR quando utilizado fora de uma função pode ser acessada de qualquer lugar do código
var nome = "Diego";

// LET variavel de escopo, desta forma o valor pode ser alterado dentro do escopo que foi declarada.
let nome = "Diego";

// CONST seu valor e imutavel apos a sua declaração.
const nome = "Diego";
